1) 선언부 - 변수 선언 - 변수의 종류
  * 일반 변수 : '스칼라 변수' 라고도 함
                    형식) 변수명 데이터형(VARCHAR2, DATE, NUMBER, CLOB) 이 순서로 작성 

 *  %TYPE : 실제 테이블의 컬럼의 데이터형을 가지고 올 때 사용
                 형식) 변수명 테이블명.컬러명%TYPE -> 데이터형을 모를 때, 가장 많이 사용되는 데이터형이다 

 *  %ROWTYPE : 테이블에 있는 컬럼의 모든 데이터형을 읽는다, 한개의 테이블만 가능
                        형식) 변수 테이블명%ROWTYPE -> 자바로 따지면 VO

 *  RECORD : 테이블 여러개 연결 -> JOIN, SUBQUERY => 사용자 정의 데이터형
 -----------------------------------------------------------------------------------------------// 단점: ROW는 한개만 사용가능              
 *  CURSOR : ROW 여러개를 동시에 저장할 때 사용 -> 자바로 전송함
                   자바의 ResulSet, ArrayList와 비슷함
