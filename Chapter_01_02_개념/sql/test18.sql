1. PL / SQL 기초
: 프로시저를 만드는 언어(기능=함수=재사용이 가능)

장점)
 프로그램은 sql문장으로 제작 -> 명령어를 모아서 처리
 속도가 빠르다
 절대 지워지지 않음
 보안이 뛰어남

단점)
 웹 개발자가 수정하기 어렵다 -> 가독성이 떨어짐

pl / sql을 가지고 만들 수 있는 세가지

1. FUNCTION : 리턴형이 있는 경우는 처리 결과값을 갖고 있다 그것이 펑션 -> 인서트나 업데이트 어려움 왜냐 리턴형이 있으니까 
2. PROCEDURE : 리턴형이 없는 경우를 프로시저(자바에서 void)
3. TRIGGER : 자동화 처리 -> 자바 자체에 소스코딩이 없음


예) 물류창고

  출고 = 재고
  입고 = 재고
-> 재고부분이 일치가 돼야해서 trigger 부분

