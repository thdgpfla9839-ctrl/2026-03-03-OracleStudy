 4.  제약조건 형식
 
 CREATE TABLE table_name(
          컬럼명 데이터형 [제약조건][제약조건] -> NOT NULL과 DEFAULT는 데이터형 뒤에 온다
          컬럼명 데이터형 [제약조건][제약조건]
          컬럼명 데이터형 [제약조건][제약조건]
          [제약조건] -> PK,UK,CK,FK
          [제약조건]
);
         
* DEFAULT - CONSTRAINT(제약조건) - NOT NULL 순서로 작성한다
 -> 제약조건은 여러개가 들어올 수 있어 디폴트가 밀려날 수 있단말야 그래서 그렇대요  
