    3. 제약조건 설정

    NOT NULL : 컬럼 데이터형 NOT NULL -> 기본형
                     컬럼 데이터형 CONSTRAINT 제약조건명 NOT NULL -> 테이블 값이 없는 경우 유지 /보수의 문제 발생
                                                             
    UNIQUE : 컬럼 데이터형 UNIQUE -> 기본형
                  컬럼 데이터형,
                  CONSTRAINT 제약조건명 UNIQUE (컬럼명) 
                  
    CHECK : 칼럼 데이터형 CHECK (컬럼명,IN(...))
                 컬럼 데이터형,
                  CONSTRAINT 제약조건명 CHECK (컬럼명)  

    PRIMARY KEY : 컬럼데이터형 PRIMARY KEY
                          컬럼 데이터형,
                           CONSTRAINT 제약조건명 PRIMARY KEY (컬럼명) 

    FOREIGN KEY :  
                          컬럼 데이터형,
                          CONSTRAINT 제약조건명 FOREIGN KEY (컬럼명)
                         REFERENCES 참조테이블 (참조컬럼) 

    DEFAULT : 컬럼 데이터형 DEFAULT 값
                     -> 데이터형 : VARCHAR2(1 ~ 4000), CLOB , CHAR(1 ~ 2000)
                                        NUMBER , NUMBER(2,1) => 8,2
                                        DATE
