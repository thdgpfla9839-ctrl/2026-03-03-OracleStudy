1. 이너조인
    : 두개 이상의 테이블에서 공통으로 존재하는 값을 이용해서 데이터만 조회한다
      개념상으로 교집합으로 이해하자
      가장 많이 사용되는 조인 -> 디폴트값이다
      조건이 맞는 경우에는 row 전체 데이터를 추출할 수 있다 => 행을 반환

2. 문법형식
    : select 출력하는 데이터
      from table1, table2
      where table1.col=table2.col => 별칭 사용이 가능하다
3. EQUI_JOIN
   : 같은 값인 경우
     ( = )을 의미함
4. NON_EQUI_JOIN
   : 범위에 포함된 경우에 사용
     (BETWEEN , 비교연산자, 논리연산자)

5. 셀프조인
    : 같은 테이블로 조인
      반드시 구분이 필요하다 -> 테이블 별칭으로 구분한다
        예) from  emp ,  emp -> 구분이 안돼
             from  emp e1 , emp e2 -> 별칭을 부여해 구분을 한다

5. 이너조인의 단점
    : null인 경우 처리를 못한다 
----------------------------------------------------------------------------------------------------
1. 아우터 조인
   :한쪽 테이블을 기준으로 봤을 때 모든 데이터를 출력 그러나 null인 경우는 출력이 안 된다 => 이걸 해결하는게 아우터 조인

    1) 기준을 왼쪽으로 잡는 경우 => LEFT OUTER JOIN
       : 교집합을 가져오되, 왼쪽을 추가로 가져온다 => INTERSECT + MINUS A-B
    2) 기준을 오른쪽으로 잡는 경우 => RIGHT OUTER JOIN
       : 교집합을 가져오되, 오른쪽을 추가로 가져온다 => INTERSECT + MINUS B-A
    3) FULL OUTER JOIN
       : 교집합을 가져오고, 양쪽을 전부 가져온다 => UNION ALL

* 번외) 프라이머리키는 자동인덱스를 갖고 있어 속도가 빠르다
