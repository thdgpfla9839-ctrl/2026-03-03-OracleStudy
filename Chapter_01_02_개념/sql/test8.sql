JOIN 
 생긴 이유 : 정규화를 하게 되면 테이블이 많이 나눠진다
                 연결해서 필요한 데이터를 추출하는 과정
 => 조인은 select에서만 사용이 가능하다

종류) 
        오라클 조인(오라클에서만 사용이 가능)
        ANSI 조인(데이터베이스 표준)
-------------------------------------------------------------------------------------------------------------------------------------
           1. INNER JOIN : 교집합(같은 값일 떄 값을 가져오다/ =일때), 가장 많이 사용되는 조인 / NULL은 처리가 안된다
                                1) EUQI_JOIN : =
                                2) NON_EUQI_JOIN : =아닌 연산자, 범위

           2. OUTER JOIN : NULL값 처리가 가능 -> 이너 조인을 보완
                                  1) LEFT OUTER JOIN : from tabke1,table2 시 왼쪽에 처리가 안된 데이터 읽기
                                  2) RIGHT OUTER JOIN : from tabke1,table2 시 오른쪽에 처리가 안된 데이터 읽기
                                  3) FULL OUTER JOIN : from tabke1,table2 시 양쪽에 처리가 안된 모든 데이터 읽기                 
                                   => Admin에서 주로 사용
