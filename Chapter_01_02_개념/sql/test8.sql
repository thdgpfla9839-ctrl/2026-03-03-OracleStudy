 => 두개 이상의 테이블을 연결해서 출력에 필요한 데이터를 추출하는 과정이 조인이다

조인의 형식)
               * INNER JOIN :   
                                   1. 오라클 조인 :
                                         select A.col , B.col
                                         from A,B
                                         where A.col = B.col

                                         A를 객체
                                         col를 변수로 본다
                                        -----------------------------
                                         select a.col , b.col => 예를들어 a.col , b.col1,b.co2에서 a를 생략 가능하다
                                         from A a,B b
                                         where a.col = b.col
                                          => 테이블에 별칭 사용이 가능하다
                                                같은 컬럼명 반드시 구분
                                                다른 컬럼명 생략이 가능




                                    2. ANSI 조인 :
                                         select A.col , B.col
                                         from A JOIN B
                                         on A.col=B.col

                   => 데이터를 합쳐서 한번에 출력할 목적을 갖고 있다(오라클에서는 조인)   
                         자바에서 합쳐주는 역할은 포함 클래스가.....
                           class A
                           { int a;
                            }
                             class B
                             { A aa;
                               int b;
                             }
                  => 여러 테이블 정보를 동시에 출력이 필요한 경우 사용한다
