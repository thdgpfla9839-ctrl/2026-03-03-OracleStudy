from emp;

--------------------------------------------
exists -> row가 존재하는지 확인(성능을 최적화) -> 조건이 만족하면 true -> 트루면 다음문장 실행 안 함
ex)
select *
from emp e
where exists( select 1 
                  from emp 
                  where deptno =10);
         

