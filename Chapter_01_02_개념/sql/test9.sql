JOIN  :
1. 테이블 두개에서 필요한 데이터 추출하는 방식 : 조인
2. 컬럼명이 동일 / 같은 값을 가지고 있는 경우 : 조인 이용
3. 컬럼명이 동일시에는 반드시 테이블.컬럼 / 별칭.컬럼을 해준다
4. NATURAL JOIN / JOIN ~ USING : 같은 컬럼명이 존재해야한다
                                              NON_EQUI_JOIN에서는 사용이 불가능하다

5. 조인의 조건 
  : 1) 컬럼은 상관없고, 같은 데이터를 가지고 있어야 한다
